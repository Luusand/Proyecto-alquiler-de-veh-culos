    private void BotonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonRegistrarActionPerformed
        // TODO add your handling code here:
        if (!nArrendador.getText().isBlank() && !cArrendador.getText().isBlank() && !dcArrendador.getText().isBlank() && !plVehiculo.getText().isBlank() && !nArrendatario.getText().isBlank() && !cArrendatario.getText().isBlank()) {
            Alquilervehiculos newalquiler = new Alquilervehiculos(nArrendador.getText(), cArrendador.getText(), dcArrendador.getText(), nArrendatario.getText(), cArrendatario.getText(), plVehiculo.getText(), TiempoContratoComboBox.getItemAt(TiempoContratoComboBox.getSelectedIndex()), MetodoDePagoComboBox.getItemAt(MetodoDePagoComboBox.getSelectedIndex()));
            listaAlquiler.add(newalquiler);
            MostrarInfor(listaAlquiler);
        }
        if (nArrendador.getText().isBlank()) {
            nArrendador.setBorder(new LineBorder(SystemColor.red));
        } else {
            nArrendador.setBorder(new LineBorder(SystemColor.black));
        }
        if (cArrendador.getText().isBlank()) {
            cArrendador.setBorder(new LineBorder(SystemColor.red));
        } else {
            cArrendador.setBorder(new LineBorder(SystemColor.black));
        }
        if (dcArrendador.getText().isBlank()) {
            dcArrendador.setBorder(new LineBorder(SystemColor.red));
        } else {
            dcArrendador.setBorder(new LineBorder(SystemColor.black));
        }
        if (plVehiculo.getText().isBlank()) {
            plVehiculo.setBorder(new LineBorder(SystemColor.red));
        } else {
            plVehiculo.setBorder(new LineBorder(SystemColor.black));
        }
        if (nArrendatario.getText().isBlank()) {
            nArrendatario.setBorder(new LineBorder(SystemColor.red));
        } else {
            nArrendatario.setBorder(new LineBorder(SystemColor.black));
        }
        if (cArrendatario.getText().isBlank()) {
            cArrendatario.setBorder(new LineBorder(SystemColor.red));
        } else {
            cArrendatario.setBorder(new LineBorder(SystemColor.black));
        }

    }//GEN-LAST:event_BotonRegistrarActionPerformed

