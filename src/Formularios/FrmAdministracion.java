    private void llenarTiempoContratoComboBox() {
        ArrayList<String> opcionesTiempo = new ArrayList<>();
        opcionesTiempo.add("1 año");
        opcionesTiempo.add("3 años");
        opcionesTiempo.add("5 años");
        DefaultComboBoxModel<String> modeloComboTiempo = new DefaultComboBoxModel<>();
        for (String opcion : opcionesTiempo) {
            modeloComboTiempo.addElement(opcion);
        }
        TiempoContratoComboBox.setModel(modeloComboTiempo);
    }

    private void llenarMetodoPagoComboBox() {
        ArrayList<String> opcionesPago = new ArrayList<>();
        opcionesPago.add("Efectivo");
        opcionesPago.add("Transferencia");
        DefaultComboBoxModel<String> modeloComboPago = new DefaultComboBoxModel<>(opcionesPago.toArray(new String[opcionesPago.size()]));
        MetodoDePagoComboBox.setModel(modeloComboPago);
    }

    public void MostrarInfor(ArrayList<Alquilervehiculos> alquiler) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(encabezado);
        for (int i = 0; i < alquiler.size(); i++) {
            modelo.addRow(new Object[]{
                alquiler.get(i).getCedulaArrendador(),
                alquiler.get(i).getNombreArrendador(),
                alquiler.get(i).getDireccionArrendatario(),
                alquiler.get(i).getPlacaVehiculo(),
                alquiler.get(i).getNombreArrendatario(),
                alquiler.get(i).getCedulaArrendatario(),
                alquiler.get(i).getTiempoContrato(),
                alquiler.get(i).getFormaDePago(),});
        }
        tableListaAlquiler.setModel(modelo);
    }

