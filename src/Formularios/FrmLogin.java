    public void IniciarSesion(String usuario, String contra){
        FrmAdministracion FromInterfaz = new FrmAdministracion();
        if (admin.equals(usuario) && adminContra.equals(contra)){
            JOptionPane.showMessageDialog(rootPane, "Inicio de Administrador exitoso");
            FromInterfaz.setVisible(true);
            this.dispose();
        }else if (user.equals(usuario) && userContra.equals(contra)){
            JOptionPane.showMessageDialog(rootPane, "Inicio de Usuario exitoso");
            FromInterfaz.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Error de ingreso");
        }
    }

